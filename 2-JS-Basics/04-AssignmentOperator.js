/*
    ------ ASSIGNMENT OPERATORS ------
    |-----------------------------|-------------|
    | Operator  | Example         | Same As     |
    |-----------------------------|-------------|
    | =         | x = y           | x = y       |
    | +=        | x += y          | x = x + y   |
    | -=        | x -= y          | x = x - y   |
    | *=        | x *= y          | x = x * y   |
    | /=        | x /= y          | x = x / y   |
    | %=        | x %= y          | x = x % y   |
    |-------------------------------------------|
    
*/

