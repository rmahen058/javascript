// ------ JAVASCRIPT OPERATORS ------

/*
    ------ ARITHMETIC OPERATORS ------
    |--------------------------------|
    | Operator  | Description        |
    |--------------------------------|
    | +         | Addition           |
    | -         | Substraction       |
    | *         | Multiplication     |
    | /         | Division           |
    | %         | Modulus            |
    | ++        | Increment          |
    | --        | Decrement          |
    |________________________________|
    
*/

var x = 100 + 50;
console.log(x);

var a = 50;
var b = 25;
x = a + b;
console.log(x);

x = (11 + 1) * a + b;
console.log(x);

var y = 5;
y++;
console.log(y);

y--;
console.log(y);
